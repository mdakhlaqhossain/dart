void main(){
   var var1 = 10, var2 = 20;
   print("var1 + var2 is: ${var1 + var2}");
   print("var1 - var2 is: ${var1 - var2}");
   print("var1 * var2 is: ${var1 * var2}");
   print("var1 / var2 is: ${var1 / var2}");
   print("var1 % var2 is: ${var1 % var2}");
   print("- (var1 - var2) is: ${-(var1 - var2)}");
}